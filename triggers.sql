-- A. Table d'historique -------------------------------------------------------
DROP TABLE glpi_history CASCADE CONSTRAINTS;

CREATE TABLE glpi_history (
  id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  table_name  VARCHAR2(64)  NOT NULL,
  record_id   NUMBER        NOT NULL,
  action      VARCHAR2(10)  NOT NULL,
  changed_by  VARCHAR2(64)  DEFAULT USER NOT NULL,
  changed_at  TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
  old_data    CLOB,
  new_data    CLOB,
  CONSTRAINT chk_history_action CHECK (action IN ('INSERT','UPDATE','DELETE'))
)
TABLESPACE TS_GLPI_REF;

CREATE INDEX idx_hist_table_rec  ON glpi_history (table_name, record_id) TABLESPACE TS_GLPI_INDX;
CREATE INDEX idx_hist_changed_at ON glpi_history (changed_at)            TABLESPACE TS_GLPI_INDX;


-- B. Package d'audit (point d'ecriture unique dans glpi_history) --------------
CREATE OR REPLACE PACKAGE pkg_audit AS
  PROCEDURE log_change(
    p_table   IN VARCHAR2,
    p_record  IN NUMBER,
    p_action  IN VARCHAR2,
    p_old     IN CLOB DEFAULT NULL,
    p_new     IN CLOB DEFAULT NULL
  );
END pkg_audit;
/

CREATE OR REPLACE PACKAGE BODY pkg_audit AS
  PROCEDURE log_change(
    p_table   IN VARCHAR2,
    p_record  IN NUMBER,
    p_action  IN VARCHAR2,
    p_old     IN CLOB DEFAULT NULL,
    p_new     IN CLOB DEFAULT NULL
  ) IS
  BEGIN
    INSERT INTO glpi_history (table_name, record_id, action, old_data, new_data)
    VALUES (p_table, p_record, p_action, p_old, p_new);
  END log_change;
END pkg_audit;
/


-- C. Triggers d'integrite -----------------------------------------------------

-- Interdit changer de campus pour une ligne de lieu : deplacer un lieu = INSERT
-- nouveau cote destinataire + migration des enfants ou autre stratégie fonctionnelle.
CREATE OR REPLACE TRIGGER tr_loc_no_chg_entity
BEFORE UPDATE OF entities_id ON glpi_locations
FOR EACH ROW
BEGIN
  IF :OLD.entities_id <> :NEW.entities_id THEN
    RAISE_APPLICATION_ERROR(-20005,
      'Changement interdit pour glpi_locations.entities_id (ancien=' || :OLD.entities_id ||
      ', nouveau=' || :NEW.entities_id || '). Migrer avec un script dedie.');
  END IF;
END;
/

-- Un ticket et son equipement doivent etre du meme site.
CREATE OR REPLACE TRIGGER tr_ticket_equip_entity
BEFORE INSERT OR UPDATE OF equipment_id, entities_id ON glpi_tickets
FOR EACH ROW
DECLARE
  v_equip_ent NUMBER;
BEGIN
  IF :NEW.equipment_id IS NOT NULL THEN
    SELECT entities_id INTO v_equip_ent
      FROM glpi_equipments
     WHERE id = :NEW.equipment_id;

    IF v_equip_ent <> :NEW.entities_id THEN
      RAISE_APPLICATION_ERROR(-20002,
        'Ticket entities_id=' || :NEW.entities_id ||
        ' incoherent avec equipment entities_id=' || v_equip_ent);
    END IF;
  END IF;
END;
/

-- Si un ticket refere un lieu physique, ce lieu doit appartenir au meme campus que le ticket.
CREATE OR REPLACE TRIGGER tr_ticket_loc_entity
BEFORE INSERT OR UPDATE OF locations_id, entities_id ON glpi_tickets
FOR EACH ROW
DECLARE
  v_loc_ent NUMBER;
BEGIN
  IF :NEW.locations_id IS NOT NULL THEN
    BEGIN
      SELECT entities_id INTO v_loc_ent
        FROM glpi_locations
       WHERE id = :NEW.locations_id;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20006,
          'Lieu inexistant pour le ticket : locations_id=' || :NEW.locations_id);
    END;
    IF v_loc_ent <> :NEW.entities_id THEN
      RAISE_APPLICATION_ERROR(-20008,
        'Ticket entities_id=' || :NEW.entities_id ||
        ' incoherent avec glpi_locations.entities_id=' || v_loc_ent ||
        ' (locations_id=' || :NEW.locations_id || ')');
    END IF;
  END IF;
END;
/

