-- D. Triggers d'historique ----------------------------------------------------

-- Audit du parc materiel.
CREATE OR REPLACE TRIGGER tr_hist_equipments
AFTER INSERT OR UPDATE OR DELETE ON glpi_equipments
FOR EACH ROW
DECLARE
  v_old CLOB;
  v_new CLOB;
  v_id  NUMBER;
  v_act VARCHAR2(10);
BEGIN
  IF INSERTING THEN
    v_act := 'INSERT'; v_id := :NEW.id;
  ELSIF UPDATING THEN
    v_act := 'UPDATE'; v_id := :NEW.id;
  ELSE
    v_act := 'DELETE'; v_id := :OLD.id;
  END IF;

  IF NOT INSERTING THEN
    v_old := 'id='        || :OLD.id        ||
             ';name='     || :OLD.name      ||
             ';itemtype=' || :OLD.itemtype  ||
             ';ent='      || :OLD.entities_id ||
             ';loc='      || :OLD.locations_id ||
             ';ip='       || :OLD.ipaddresses_id;
  END IF;
  IF NOT DELETING THEN
    v_new := 'id='        || :NEW.id        ||
             ';name='     || :NEW.name      ||
             ';itemtype=' || :NEW.itemtype  ||
             ';ent='      || :NEW.entities_id ||
             ';loc='      || :NEW.locations_id ||
             ';ip='       || :NEW.ipaddresses_id;
  END IF;

  pkg_audit.log_change('glpi_equipments', v_id, v_act, v_old, v_new);
END;
/

